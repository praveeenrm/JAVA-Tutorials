
// If condition

public class If_Condition {

	public static void main(String[] args) {

		int age = 19;

		if (age > 18) {
			
			System.out.println("You are allowed.");
			
		} else if (age > 12) {
			
			System.out.println("You are allowed but with parents.");
			
		} else {
			
			System.out.println("You are NOT allowed.");
			
		}

	}

}

// OUTPUT

// You are allowed.
