
public class ForLoop {

	public static void main(String[] args) {
		
		for(int i = 0; i < 5; i++) {
			
			System.out.println("The value of i is " + i + ".");
			
		}
		
	}

}

// OUTPUT

//The value of i is 0.
//The value of i is 1.
//The value of i is 2.
//The value of i is 3.
//The value of i is 4.