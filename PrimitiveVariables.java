
// Primitive Data Types
// There are 8 primitive data types

public class PrimitiveVariables {

	public static void main(String[] args) {
		
		// INTEGER Types
		byte b = 12; // 8 bit, -128 to 127
		short s = 1000; // 16 bit, -32768 to 32767
		int age = 23; // 32 bit, long range
		long l = 2748741; // 64 bit, longest
		
		// FLOATING POINT Types
		float height = 6.2f; // 32 bit  // Have to use 'f'. 
		double weight = 80; // 64 bit
		
		boolean isCool = true; // 16 bit
		
		char letter = 'R'; // 16 bit // char takes single quotes
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(age);
		System.out.println(l);
		System.out.println(height);
		System.out.println(weight);
		System.out.println(isCool);
		System.out.println(letter);
		
		
	}

}

// OUTPUT

//12
//1000
//23
//2748741
//6.2
//80.0
//true
//R
